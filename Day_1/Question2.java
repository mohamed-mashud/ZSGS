// 2. Write a program in which, declare all data types like integer, double, float, long integer, character and byte data and print them.
public class Question2 {
    public static void main(String[] args) {
        // Integer data type
        int intVal = 100;

        // Double data type
        double doubleVal = 12345.6789;

        // Float data type 
        float floatVal = 12.34f;

        // Long data type 
        long longVal = 1234567890L;

        // Character data type
        char charVal = 'A';

        // Byte data type
        byte byteVal = 127;

        // Printing all the values
        System.out.println("Integer Value: " + intVal);
        System.out.println("Double Value: " + doubleVal);
        System.out.println("Float Value: " + floatVal);
        System.out.println("Long Value: " + longVal);
        System.out.println("Character Value: " + charVal);
        System.out.println("Byte Value: " + byteVal);
    }
}
